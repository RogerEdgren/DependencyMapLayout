

// a super simple class used for storing line points in a container
public class LineInt {

	public int startX;
	public int startY;
	public int endX;
	public int endY;
	
	public LineInt(int _startX, int _startY, int _endX, int _endY)
	{
		startX = _startX;
		startY = _startY;
		endX = _endX;
		endY = _endY;
	}


}
